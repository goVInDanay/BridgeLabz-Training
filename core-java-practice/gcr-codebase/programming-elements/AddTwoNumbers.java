import java.util.Scanner;
public class AddTwoNumbers {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter number 1 and number 2");
        int n1 = sc.nextInt();
        int n2 = sc.nextInt();
        System.out.println(n1 + n2);
    }
}
